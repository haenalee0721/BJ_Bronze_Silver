#include <iostream>
#include <algorithm>

using namespace std;

int main() {

	ios::sync_with_stdio(0);
	cin.tie(nullptr);

	long long a, b;
	cin >> a >> b;

	if (a >b) {
		swap(a, b); // algorithm 헤더에 들어있는 swap 함수
	}if (a == b) {
		cout << 0;
	}
	else {

	cout << b - a - 1 << "\n";
	for (long long i = a + 1; i < b; i++) {
		cout << i << " ";
	}
}

}
